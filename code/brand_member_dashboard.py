# ====== 核心函数 ======
def read_data(filepath):
    """Read the xlsx file and return a clean DataFrame"""
    df = pd.read_excel(filepath, engine='openpyxl', header=None, skiprows=1)
    
    col_names = [
        '业务品牌名称', '目标类型name', '日', 'KA广告投放计划id', '商家ID',
        '省份', '城市', '投放计划名称', 'KA账号id', '广告消耗',
        '注册会员人数', '会员注册成本', '广告ROI', '交易会员人数',
        '广告点击次数', '广告曝光次数', '会员注册率', '广告CVR',
        '广告订单数', '广告CPM', '广告CPC', '广告CTR', '广告订单人数',
        '广告订单原价交易额', '广告订单实付交易额', '广告原价金额'
    ]
    df.columns = col_names
    
    # 城市→省份映射
    df['省份'] = df['城市'].map(CITY_TO_PROVINCE)
    
    # 日期解析
    df['日'] = pd.to_datetime(df['日'].astype(str), format='%Y%m%d')
    df['月'] = df['日'].dt.month
    
    # 过滤掉省份为NaN的（如果有未映射的城市）
    unmapped = df[df['省份'].isna() & df['城市'].notna()]['城市'].unique()
    if len(unmapped) > 0:
        print(f"⚠️ 未映射的城市 ({len(unmapped)}): {unmapped[:10]}...")
    
    return df


def aggregate_by_province(df):
    """Aggregate data by province and target type"""
    agg = df.groupby(['目标类型name', '省份']).agg({
        '广告消耗': 'sum',
        '注册会员人数': 'sum',
        '广告订单实付交易额': 'sum',
        '广告ROI': 'mean',
        '广告订单数': 'sum',
        '广告点击次数': 'sum',
        '广告曝光次数': 'sum',
    }).round(2)
    
    agg['注册成本(CA/人)'] = (agg['广告消耗'] / agg['注册会员人数'].replace(0, float('nan'))).round(2)
    agg['加权ROI'] = (agg['广告订单实付交易额'] / agg['广告消耗'].replace(0, float('nan'))).round(2)
    agg['CPC'] = (agg['广告消耗'] / agg['广告点击次数'].replace(0, float('nan'))).round(4)
    agg['CPM'] = (agg['广告消耗'] / agg['广告曝光次数'] * 1000).replace(float('inf'), float('nan')).round(2)
    
    return agg


def province_overall(df):
    """Aggregate by province across all target types"""
    agg = df.groupby('省份').agg({
        '广告消耗': 'sum',
        '注册会员人数': 'sum',
        '广告订单实付交易额': 'sum',
        '广告ROI': 'mean',
        '广告订单数': 'sum',
        '广告点击次数': 'sum',
        '广告曝光次数': 'sum',
    }).round(2)
    
    agg['加权ROI'] = (agg['广告订单实付交易额'] / agg['广告消耗'].replace(0, float('nan'))).round(2)
    return agg


