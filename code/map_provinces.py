# 实际读取数据中的城市列表，映射省份
with zipfile.ZipFile('/Users/edy/Desktop/品牌会员数据.xlsx', 'r') as z:
    tree = ET.parse(z.open('xl/sharedStrings.xml'))
    root = tree.getroot()
    ns = {'s': 'http://schemas.openxmlformats.org/spreadsheetml/2006/main'}
    sis = root.findall('.//s:si', ns)
    strings = [si.find('.//s:t', ns).text if si.find('.//s:t', ns) is not None else '' for si in sis]
    
    raw = z.open('xl/worksheets/sheet1.xml').read().decode()
    rows = re.findall(r'<row[^>]*>(.*?)</row>', raw, re.DOTALL)
    
    cities_found = set()
    for row_xml in rows[1:]:
        cells = re.findall(r'(<c[^>]*>.*?</c>)', row_xml, re.DOTALL)
        row = {}
        for cell_str in cells:
            rm = re.search(r'r="([^"]+)"', cell_str)
            if not rm: continue
            ref = rm.group(1)
            m2 = re.match(r'([A-Z]+)', ref)
            if not m2: continue
            col = 0
            for c in m2.group(1):
                col = col * 26 + (ord(c) - ord('A') + 1)
            col -= 1
            is_str = 't="s"' in cell_str
            vm = re.search(r'<v>(.*?)</v>', cell_str)
            if vm:
                val = vm.group(1)
                if is_str:
                    try: val = strings[int(val)]
                    except: pass
                row[col] = val
        
        city = row.get(5)
        if city and city not in ('0','NULL','',None):
            cities_found.add(city)
    
    # 打印所有城市及其映射到的省份
    mapped = {}
    unmapped = []
    for c in sorted(cities_found):
        if c in city_to_province:
            prov = city_to_province[c]
            if prov not in mapped:
                mapped[prov] = []
            mapped[prov].append(c)
        else:
            unmapped.append(c)
    
    print("=== 已映射的省份 ===")
    for p, cities in sorted(mapped.items()):
        print(f"  {p}: {cities}")
    
    print(f"\n=== 未映射的城市 ({len(unmapped)}) ===")
    print(unmapped)
    
    # 输出省份映射的JS代码
    print("\n=== JS code ===")
    for c, p in sorted(city_to_province.items()):
        if c in cities_found:
            print(f"  '{c}': '{p}',")
